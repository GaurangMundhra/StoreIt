export const deleteFile = async ({
  fileId,
  bucketFileId,
  path,
}: DeleteFileProps) => {
  const { databases, storage } = await createAdminClient();

  try {
    const deletedFile = await databases.deleteDocument(
      appwriteConfig.databaseId,
      appwriteConfig.filesCollectionId,
      fileId,
    );

    if (deletedFile) {
      await storage.deleteFile(appwriteConfig.bucketId, bucketFileId);
    }

    revalidatePath(path);
    return parseStringify({ status: "success" });
  } catch (error) {
    handleError(error, "Failed to rename file");
  }
};

// ============================== TOTAL FILE SPACE USED
type FileCategory = "image" | "document" | "video" | "audio" | "other";

export async function getTotalSpaceUsed() {
  try {
    const { databases } = await createSessionConfig();
    const currentUser = await getCurrentUser();
    if (!currentUser) throw new Error("User is not authenticated.");

    const totalSpace: Record<FileCategory, { size: number; latestDate: string }> & {
      used: number;
      all: number;
    } = {
      image: { size: 0, latestDate: "" },
      document: { size: 0, latestDate: "" },
      video: { size: 0, latestDate: "" },
      audio: { size: 0, latestDate: "" },
      other: { size: 0, latestDate: "" },
      used: 0,
      all: 2 * 1024 * 1024 * 1024, // 2GB
    };

    const files = await databases.listDocuments(
      appwriteConfig.databaseId,
      appwriteConfig.filesCollectionId,
      [Query.equal("owner", [currentUser.$id])],
    );

    if (files && Array.isArray(files.documents)) {
      files.documents.forEach((file) => {
        if (file && typeof file === "object") {
          const rawType = file.type;
          const fileType: FileCategory = ["image", "document", "video", "audio", "other"].includes(rawType)
            ? rawType
            : "other";

          const fileSize =
            typeof file.size === "number" && !isNaN(file.size) ? file.size : 0;

          totalSpace[fileType].size += fileSize;
          totalSpace.used += fileSize;

          if (
            file.$updatedAt &&
            (!totalSpace[fileType].latestDate ||
              new Date(file.$updatedAt) > new Date(totalSpace[fileType].latestDate))
          ) {
            totalSpace[fileType].latestDate = file.$updatedAt;
          }
        }
      });
    } else {
      console.warn("No valid documents found in files response:", files);
    }

    return parseStringify(totalSpace);
  } catch (error) {
    handleError(error, "Error calculating total space used:");
  }
}
